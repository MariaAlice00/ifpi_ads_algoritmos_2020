# Se você correr 10 quilômetros em 42 minutos e 42 segundos, qual é o seu passo médio?
# Passo médio = tempo por milha em minutos e segundos.
# Qual é a sua velocidade média em milhas por hora?
# 10 quilômetros = 6 milhas = 2562 segundos
tempo = 2562 / 6
minuto = tempo // 60
segundo = tempo % 60
print(f'O tempo por milha é de {minuto} minutos e {segundo} segundos')


