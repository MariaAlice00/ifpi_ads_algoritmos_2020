# Leia o valor da base e altura de um triângulo, calcule e escreva sua área
# Área = (base * altura) / 2

# Entrada
base = float(input('Digite o valor da base: '))
altura = float(input('Digite o valor da altura: '))

# Processamento
area = (base * altura) / 2
area = round(area, 2)

# Saída
print('O resultado da área é: ', area)

